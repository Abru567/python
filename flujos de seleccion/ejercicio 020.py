"""
Escribir un programa que permita ingresar dos cadenas
de caracteres e indicar si son iguales o distintas.
"""

primeracad = input("Ingrese una cadena de caracteres: ")
segundacad = input("Ingrese otra cadena de caracteres: ")

if primeracad == segundacad:
    print("las cadenas de caracteres son iguales")
else:
    print("las cadenas de caracteres son distintas")